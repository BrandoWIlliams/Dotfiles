    "CursorLineNr",
    "EndOfBuffer",
  },
  extra_groups = {
    "NvimTreeNormal",
  }, -- table: additional groups that should be cleared
  exclude_groups = {
    "NormalFloat",
  }, -- table: groups you don't want to clear
}

